/**
 * Rotates through a pool of real-world User-Agent strings.
 * The initial index is randomized so different instances start at different positions.
 * `get()` increments first, then returns — the constructor index acts as a pre-position.
 */
export class RotatingUserAgentProvider implements UserAgentProvider {
  private index: number;

  constructor() {
    this.index = Math.floor(Math.random() * USER_AGENTS.length);
  }

  get(): string {
    this.index = (this.index + 1) % USER_AGENTS.length;
    return USER_AGENTS[this.index];
  }
}

